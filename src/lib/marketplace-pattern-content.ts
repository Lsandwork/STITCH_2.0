export const MP_DACHSHUND_PATTERN = `MATERIALS
• Worsted-weight (#4) yarn: 180 yds brown, 40 yds cream, 20 yds black
• 4.0 mm (G-6) crochet hook
• 9 mm safety eyes, polyester stuffing, tapestry needle, stitch marker

GAUGE: 6 sc × 7 rounds = 1 inch in single crochet

FINISHED SIZE: 9 inches nose to tail

ABBREVIATIONS: sc = single crochet, inc = 2 sc in same st, dec = sc2tog, MR = magic ring, sl st = slip stitch

BODY (worked in continuous rounds — use a stitch marker)
1. With brown yarn, make a magic ring. Work 6 sc into the ring. Pull the tail tight to close the center. (6 stitches)
2. Round 2: Work 2 sc (increase) in every stitch around. You should have 12 stitches.
3. Round 3: *Sc in next stitch, inc in next stitch* — repeat 6 times. (18 stitches)
4. Round 4: *Sc in next 2 stitches, inc in next stitch* — repeat 6 times. (24 stitches)
5. Rounds 5–22: Sc in each stitch around with no increases. This forms the long dachshund body. Add stuffing lightly every 4 rounds.
6. Round 23: *Sc in next 5 stitches, dec* — repeat 4 times. (20 stitches)
7. Round 24: *Sc in next 3 stitches, dec* — repeat 4 times. (16 stitches) Stuff the body firmly before continuing.
8. Round 25: *Sc in next 2 stitches, dec* — repeat 4 times. (12 stitches)
9. Round 26: *Sc, dec* — repeat 4 times. (8 stitches) Fasten off brown, leaving a 12-inch tail.

HEAD (attach cream yarn to last round of body)
10. Round 27: With cream, inc in each of the 8 stitches around. (16 stitches)
11. Round 28: *Sc, inc* — repeat 8 times. (24 stitches)
12. Rounds 29–32: Sc in each stitch around. (24 stitches) Insert safety eyes between rounds 30 and 31, about 6 stitches apart.
13. Round 33: *Sc in next 4 stitches, dec* — repeat 4 times. (20 stitches) Begin stuffing the head.
14. Round 34: *Sc in next 3 stitches, dec* — repeat 4 times. (16 stitches)
15. Round 35: *Sc in next 2 stitches, dec* — repeat 4 times. (12 stitches) Finish stuffing.
16. Round 36: *Sc, dec* — repeat 4 times. (8 stitches)
17. Round 37: Dec around. (4 stitches) Fasten off. Weave tail through remaining loops and pull closed.

EARS (make 2 with brown)
18. Ch 6. Sc in 2nd ch from hook and in each ch across. (5 stitches) Turn and work 4 more rows of sc.
19. Fold ear in half and sc through both layers along the top edge to close. Leave a long tail for sewing.
20. Sew ears to top of head between rounds 29 and 31, about 4 stitches apart.

PAWS (make 4 small ovals with cream)
21. Ch 4. Sc in 2nd ch and in each ch across. Work 3 rows total. Sew paws evenly along the underside of the body.

NOSE & FINISHING
22. With black yarn, embroider an oval nose on the cream snout and a short line below it.
23. Weave in all ends inside the body. Shape gently with your hands.`;

