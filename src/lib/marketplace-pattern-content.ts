export const MP_GRANNY_THROW_PATTERN = `MATERIALS
• Aran-weight (#4): 400 yds Color A, 300 yds each Colors B & C, 200 yds Color D (border)
• 5.5 mm (I-9) hook, stitch markers, blocking pins

GAUGE: One 4-round square = 6 inches after blocking

FINISHED SIZE: 48 × 60 inches (20 squares: 4 wide × 5 tall)

ONE GRANNY SQUARE (make 20 total)
1. With Color A, ch 4 and join with sl st to form a ring.
2. Round 1: Ch 3 (counts as dc), 2 dc in ring, ch 2, *3 dc in ring, ch 2* — repeat 3 times. Join to top of ch-3. (4 corners)
3. Round 2: Sl st to corner space. Ch 3, 2 dc, ch 2, 3 dc in same corner. *3 dc in next side space, (3 dc, ch 2, 3 dc) in corner* — repeat around. Join.
4. Round 3: Join Color B in any corner. Repeat round 2 logic — work 3-dc groups along each side with (3 dc, ch 2, 3 dc) at corners.
5. Round 4: Join Color C. Repeat round 3. Fasten off and weave in ends. Block square to 6 inches.

LAYOUT
6. Arrange 20 squares in a 4 × 5 grid on the floor. Alternate Color B placement so no two identical centers touch.

JOIN-AS-YOU-GO (recommended — fewer ends)
7. Hold two squares right sides together. Join Color D in corner ch-2 space of square 1.
8. Ch 1, sc through both squares along the shared edge. At the corner, work 3 sc through both layers.
9. Continue joining squares row by row: first make a strip of 4 squares, then join strips together along long edges.
10. As you join, each new square connects to its neighbor on one side during the final round of sc join.

BORDER
11. Round 1: With Color D, join at any corner. Work 3 dc in each corner ch-2 space. Along each side, work 3-dc groups evenly (about 2 groups per square edge).
12. Round 2: Ch 1, sc in each st around, with 3 sc in each corner.
13. Round 3: *Sc in next 2 sts, skip 1 st, 5 dc in next st, skip 1 st* — repeat around for a scalloped edge. Join and fasten off.

FINISHING
14. Weave in remaining ends. Wash gently on delicate cycle or hand wash. Lay flat to dry and block to final measurements.`;

export const MP_CARDIGAN_PATTERN = `MATERIALS
• DK-weight (#3): 1,400–1,800 yds main color (amount varies by size)
• 6.0 mm (J-10) hook, stitch markers, tapestry needle, 4 buttons

GAUGE: 14 hdc × 10 rows = 4 inches

SIZES: XS (32"), S (36"), M (40"), L (44"), XL (48"), 2XL (52") bust

YOKE (top-down — start with size M)
1. Ch 82 (or your size's starting chain). Row 1: Hdc in 3rd ch from hook and in each ch across. (80 hdc)
2. Place markers at center 20 sts (back neck) and at each side seam point (20 sts from each end).
3. Row 2 (increase row): Hdc to 1 st before marker, inc (2 hdc in next st), hdc to marker, inc, hdc across back, inc, hdc to next marker, inc, hdc to end.
4. Repeat increase row every other row 12 times total. Try on to check shoulder fit — yoke should reach just below armpits.
5. Count stitches for front panels, sleeves, and back. Place markers to divide sleeve stitches from body stitches.

BODY SEPARATION
6. Work across right front, chain sleeve length + 4 for underarm, skip sleeve stitches, work across back, chain underarm, work left front.
7. Join to work in the round for the body. Work even in hdc until body measures 14 inches from underarm (or desired length).

SLEEVES
8. Rejoin yarn at underarm chain. Pick up 4 extra hdc along underarm for comfort. Work hdc in the round.
9. Decrease 1 st each side every 4th round until sleeve reaches wrist. Work 6 rounds of sc BLO for ribbed cuff.
10. Repeat for second sleeve.

BUTTON BAND & COLLAR
11. With right side facing, pick up 1 sc per row along front edge. Work 3 rows of sc for button band.
12. Ch 1, turn, sc in each st. On row 2, make buttonholes: ch 2, skip 2 sts at button placements spaced every 3 inches.
13. Pick up sts around neckline. Work 4 rounds of hdc for a rolled collar. Join and fasten off.

POCKETS (optional)
14. Crochet two 6 × 6 inch hdc rectangles. Sew to front panels 2 inches below underarm, centered.

FINISHING
15. Sew on buttons opposite buttonholes. Weave in all ends. Block lightly to measurements.`;

